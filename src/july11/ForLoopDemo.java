package july11;

public class ForLoopDemo {
    public static void main(String[] args) {

        // for loop - 1 - 10
        //  initi       condition   // increment
        int i = 1;  // 1 init  , 2 check condition 3. print statement
        for (; i <= 10 ; i++) {  // 4 increment/ decrement

            System.out.println(i); // print this line
        }
    }
}

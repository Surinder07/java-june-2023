package july15;

public class NestedLoopsDemo {
    public static void main(String[] args) {


        for (int i = 0; i < 5 ; i++) {// outer for loop , rows
            for (int j = 0; j<= i; j++) {// inner for loop , column
                System.out.print(i);
            }
            System.out.println("");
        }
    }
}

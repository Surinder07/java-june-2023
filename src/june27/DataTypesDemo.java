package june27;

public class DataTypesDemo {
    public static void main(String[] args) {

        //  ++   increment ?   +1   , post increment and pre increment?
        //  --   decrement ?   -1
        // post increment  x++
        // pre increment    ++x
        int x = 10;
        System.out.println(x++);
        System.out.println(x);

    }
}
